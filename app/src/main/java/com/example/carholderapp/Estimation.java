package com.example.carholderapp;

public class Estimation {
    public String ACR;
    public String AmountPayable;
    public String BaldType;
    public String Excesses;
    public String OtherDeductions;
    public String UnderInsurance;

    public Estimation(String ACR,String AmountPayable,String BaldType,String Excesses,String OtherDeductions,String UnderInsurance) {

        this.ACR=ACR;
        this.AmountPayable=AmountPayable;
        this.BaldType=BaldType;
        this.Excesses=Excesses;
        this.OtherDeductions=OtherDeductions;
        this.UnderInsurance=UnderInsurance;

    }
}
